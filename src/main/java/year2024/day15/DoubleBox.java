package year2024.day15;

/**
 * Class represents a double-box for part 2 of day 15, 2024
 */
public class DoubleBox {

  int leftX, leftY, rightX, rightY;

  public DoubleBox(int leftX, int leftY, int rightX, int rightY) {
    this.leftX = leftX;
    this.leftY = leftY;
    this.rightX = rightX;
    this.rightY = rightY;
  }
}